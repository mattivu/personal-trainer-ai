function unique(values) {
  return [...new Set(values.filter(Boolean))];
}

function exercise({
  name,
  slug,
  category = "strength",
  primaryMuscle,
  secondaryMuscles = [],
  equipment = null,
  difficulty = "beginner",
  movementPattern,
  environments = ENV_GYM,
  tags = [],
  alternatives = [],
  contraindications = [],
  instructions,
}) {
  return {
    name,
    slug,
    category,
    primaryMuscle,
    secondaryMuscles: unique(secondaryMuscles),
    equipment,
    difficulty,
    movementPattern,
    environments: unique(environments),
    tags: unique(tags),
    alternatives: unique(alternatives.filter((value) => value !== slug)),
    contraindications: unique(contraindications),
    instructions,
  };
}

